@ActivityScope
public class TracePayBackPresenter extends
    BasePresenter<TracePayBackContract.Model, TracePayBackContract.View> {

  @Inject
  RxErrorHandler mErrorHandler;
  @Inject
  Application mApplication;
  @Inject
  ImageLoader mImageLoader;
  @Inject
  AppManager mAppManager;

  @Inject
  public TracePayBackPresenter(TracePayBackContract.Model model,
      TracePayBackContract.View rootView) {
    super(model, rootView);
  }

  @Override
  public void onDestroy() {
    super.onDestroy();
    this.mErrorHandler = null;
    this.mAppManager = null;
    this.mImageLoader = null;
    this.mApplication = null;
  }
}
